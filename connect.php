<?php
// การตั้งค่าการเชื่อมต่อฐานข้อมูล
$host = "localhost"; // ชื่อโฮสต์ของฐานข้อมูล (ปกติเป็น localhost)
$user = "root";      // ชื่อผู้ใช้ฐานข้อมูล (ค่าเริ่มต้นของ XAMPP คือ root)
$pass = "";          // รหัสผ่านฐานข้อมูล (ค่าเริ่มต้นของ XAMPP คือไม่มีรหัสผ่าน)
$dbname = "mydata";  // ชื่อฐานข้อมูลที่ต้องการเชื่อมต่อ

// สร้างการเชื่อมต่อ
$conn = mysqli_connect($host, $user, $pass, $dbname);

// ตรวจสอบการเชื่อมต่อ
if (!$conn) {
    die("การเชื่อมต่อล้มเหลว: " . mysqli_connect_error());
}

// ตั้งค่าให้รองรับภาษาไทย
mysqli_set_charset($conn, "utf8");

// หากเชื่อมต่อสำเร็จ ไม่ต้องแสดงข้อความใดๆ โค้ดจะทำงานต่อไป
// echo "เชื่อมต่อฐานข้อมูลสำเร็จ";
?>